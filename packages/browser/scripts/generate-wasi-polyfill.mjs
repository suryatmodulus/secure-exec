#!/usr/bin/env node
// Generates packages/browser/src/wasi-polyfill.ts from the single shared
// native WASI preview1 runner (crates/execution/assets/runners/wasi-module.js),
// wrapping it with a browser-backend host seam (globalThis.__agentOSWasiHost).
// This is item C of BROWSER-CONVERGENCE-ARCHITECTURE.md: ONE shared preview1
// runner consumed by both backends.
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(scriptDir, "..");
const repoRoot = path.resolve(packageRoot, "..", "..");

const SYNC_READ_LIMIT_BYTES = 16 * 1024 * 1024;

export const NATIVE_WASI_RUNNER_PATH = path.join(
	repoRoot,
	"crates",
	"execution",
	"assets",
	"runners",
	"wasi-module.js",
);
export const BROWSER_WASI_POLYFILL_PATH = path.join(
	packageRoot,
	"src",
	"wasi-polyfill.ts",
);

/** Build the browser wasi-polyfill.ts content from the shared native runner. */
export function buildBrowserWasiPolyfill() {
	const nativeSource = readFileSync(NATIVE_WASI_RUNNER_PATH, "utf8");
	return buildFromNativeSource(nativeSource);
}

function buildFromNativeSource(nativeSource) {

// Native substitutes the read-limit build placeholder in wasm.rs; do the same
// here so the browser bundle carries a concrete number (the host-seam ternary
// short-circuits before this token, but keep the source valid + free of the
// native-only placeholder).
const PLACEHOLDER = "__SECURE_EXEC_WASM_SYNC_READ_LIMIT_BYTES__";
if (!nativeSource.includes(PLACEHOLDER)) {
	throw new Error(
		`native WASI runner no longer contains ${PLACEHOLDER}; update generator`,
	);
}
const substituted = nativeSource
	.split(PLACEHOLDER)
	.join(String(SYNC_READ_LIMIT_BYTES));

// Escape for embedding in a JS template literal.
const escaped = substituted
	.replace(/\\/g, "\\\\")
	.replace(/`/g, "\\`")
	.replace(/\$\{/g, "\\${");

const banner = `// @generated by packages/browser/scripts/generate-wasi-polyfill.mjs
// Source: crates/execution/assets/runners/wasi-module.js (the single shared
// WASI preview1 runner). DO NOT EDIT BY HAND. Run \`pnpm generate:wasi-polyfill\`.
`;

// The browser backend host seam. Native populates __agentOSWasiHost from its own
// host globals; the browser converged worker supplies the equivalents the runner
// needs. Two pieces:
//   1. __agentOSWasiHost: builtin resolution via the guest CJS require + the sync
//      read cap. Stdio fd sync-RPC + fd-handle lookup are gated behind
//      AGENTOS_WASI_STDIO_SYNC_RPC (unset in the browser), so fd 1/2 ride
//      globalThis.process.{stdout,stderr} like a native non-sidecar run.
//   2. A module-scoped `Buffer`: the shared runner uses Node's global Buffer, but
//      the browser guest exposes no Buffer global and has no `buffer` polyfill, so
//      provide a Uint8Array-subclass shim the nested runner class closes over.
//      Native never reaches this branch (its globalThis.Buffer is truthy).
const seam = `\t\tglobalThis.__agentOSWasiHost = {
\t\t\trequireBuiltin: (name) =>
\t\t\t\tglobalThis.require(String(name).replace(/^node:/, "")),
\t\t\tsyncReadLimitBytes: ${SYNC_READ_LIMIT_BYTES},
\t\t\t// Browser fs descriptors are a JS handle table, not real host OS fds with
\t\t\t// a kernel offset, so locally-opened files must use the offset-aware file
\t\t\t// branches (explicit position) rather than host-passthrough null reads.
\t\t\tdisableLocalFdPassthrough: true,
\t\t\t// Guest stdin is delivered through the runtime process object, not a kernel
\t\t\t// fd, so read the queued bytes from process.stdin directly.
\t\t\treadStdin: (maxBytes) =>
\t\t\t\t(globalThis.process &&
\t\t\t\t\tglobalThis.process.stdin &&
\t\t\t\t\ttypeof globalThis.process.stdin.read === "function"
\t\t\t\t\t? globalThis.process.stdin.read(maxBytes)
\t\t\t\t\t: null),
\t\t\t// Queued stdin byte count for poll_oneoff readiness (does not consume).
\t\t\tstdinReadableBytes: () =>
\t\t\t\t(globalThis.process && globalThis.process.stdin
\t\t\t\t\t? Number(globalThis.process.stdin.readableLength || 0)
\t\t\t\t\t: 0),
\t\t};
\t\tconst Buffer =
\t\t\t(typeof globalThis !== "undefined" && globalThis.Buffer) ||
\t\t\t(class __AgentOsWasiBuffer extends Uint8Array {
\t\t\t\tstatic alloc(size) { return new __AgentOsWasiBuffer(size >>> 0); }
\t\t\t\tstatic allocUnsafe(size) { return new __AgentOsWasiBuffer(size >>> 0); }
\t\t\t\tstatic isBuffer(value) { return value instanceof Uint8Array; }
\t\t\t\tstatic byteLength(value, encoding) {
\t\t\t\t\tif (value instanceof Uint8Array) return value.length;
\t\t\t\t\tif (encoding === "base64") return Math.floor((String(value).replace(/=+$/, "").length * 3) / 4);
\t\t\t\t\tif (encoding === "hex") return String(value).length >> 1;
\t\t\t\t\treturn new TextEncoder().encode(String(value)).length;
\t\t\t\t}
\t\t\t\tstatic from(value, encodingOrOffset, length) {
\t\t\t\t\tif (typeof value === "string") {
\t\t\t\t\t\tconst encoding = encodingOrOffset || "utf8";
\t\t\t\t\t\tif (encoding === "base64") {
\t\t\t\t\t\t\tconst binary = atob(value);
\t\t\t\t\t\t\tconst out = new __AgentOsWasiBuffer(binary.length);
\t\t\t\t\t\t\tfor (let i = 0; i < binary.length; i += 1) out[i] = binary.charCodeAt(i) & 0xff;
\t\t\t\t\t\t\treturn out;
\t\t\t\t\t\t}
\t\t\t\t\t\tif (encoding === "hex") {
\t\t\t\t\t\t\tconst clean = String(value);
\t\t\t\t\t\t\tconst out = new __AgentOsWasiBuffer(clean.length >> 1);
\t\t\t\t\t\t\tfor (let i = 0; i < out.length; i += 1) out[i] = parseInt(clean.substr(i * 2, 2), 16);
\t\t\t\t\t\t\treturn out;
\t\t\t\t\t\t}
\t\t\t\t\t\tconst encoded = new TextEncoder().encode(value);
\t\t\t\t\t\tconst out = new __AgentOsWasiBuffer(encoded.length);
\t\t\t\t\t\tout.set(encoded);
\t\t\t\t\t\treturn out;
\t\t\t\t\t}
\t\t\t\t\tif (value instanceof ArrayBuffer) {
\t\t\t\t\t\tconst offset = encodingOrOffset || 0;
\t\t\t\t\t\tconst len = length === undefined ? value.byteLength - offset : length;
\t\t\t\t\t\tconst view = new Uint8Array(value, offset, len);
\t\t\t\t\t\tconst out = new __AgentOsWasiBuffer(view.length);
\t\t\t\t\t\tout.set(view);
\t\t\t\t\t\treturn out;
\t\t\t\t\t}
\t\t\t\t\tif (ArrayBuffer.isView(value)) {
\t\t\t\t\t\tconst view = new Uint8Array(value.buffer, value.byteOffset, value.byteLength);
\t\t\t\t\t\tconst out = new __AgentOsWasiBuffer(view.length);
\t\t\t\t\t\tout.set(view);
\t\t\t\t\t\treturn out;
\t\t\t\t\t}
\t\t\t\t\tconst arr = Array.from(value || []);
\t\t\t\t\tconst out = new __AgentOsWasiBuffer(arr.length);
\t\t\t\t\tfor (let i = 0; i < arr.length; i += 1) out[i] = arr[i] & 0xff;
\t\t\t\t\treturn out;
\t\t\t\t}
\t\t\t\tstatic concat(list, totalLength) {
\t\t\t\t\tconst chunks = Array.from(list || []);
\t\t\t\t\tif (totalLength === undefined) {
\t\t\t\t\t\ttotalLength = 0;
\t\t\t\t\t\tfor (const chunk of chunks) totalLength += chunk.length;
\t\t\t\t\t}
\t\t\t\t\tconst out = new __AgentOsWasiBuffer(totalLength >>> 0);
\t\t\t\t\tlet offset = 0;
\t\t\t\t\tfor (const chunk of chunks) {
\t\t\t\t\t\tif (offset >= out.length) break;
\t\t\t\t\t\tconst slice = offset + chunk.length > out.length ? chunk.subarray(0, out.length - offset) : chunk;
\t\t\t\t\t\tout.set(slice, offset);
\t\t\t\t\t\toffset += slice.length;
\t\t\t\t\t}
\t\t\t\t\treturn out;
\t\t\t\t}
\t\t\t\ttoString(encoding, start, end) {
\t\t\t\t\tconst view = this.subarray(start || 0, end === undefined ? this.length : end);
\t\t\t\t\tif (encoding === "base64") {
\t\t\t\t\t\tlet binary = "";
\t\t\t\t\t\tfor (let i = 0; i < view.length; i += 1) binary += String.fromCharCode(view[i]);
\t\t\t\t\t\treturn btoa(binary);
\t\t\t\t\t}
\t\t\t\t\tif (encoding === "hex") {
\t\t\t\t\t\tlet hex = "";
\t\t\t\t\t\tfor (let i = 0; i < view.length; i += 1) hex += view[i].toString(16).padStart(2, "0");
\t\t\t\t\t\treturn hex;
\t\t\t\t\t}
\t\t\t\t\treturn new TextDecoder().decode(view);
\t\t\t\t}
\t\t\t});
`;

const footer = `\t\t// Re-export the shared runner WASI class as the browser wasi module.
\t\tmodule.exports = { WASI: globalThis.__agentOSWasiModule.WASI };
\t\tmodule.exports.default = { WASI: globalThis.__agentOSWasiModule.WASI };
`;

	const out = `${banner}export const BROWSER_WASI_POLYFILL_CODE = \`
${seam}${escaped}
${footer}\`;
`;
	return out;
}

function isMainModule() {
	return (
		import.meta.url === `file://${process.argv[1]}` ||
		process.argv[1] === fileURLToPath(import.meta.url)
	);
}

if (isMainModule()) {
	writeFileSync(BROWSER_WASI_POLYFILL_PATH, buildBrowserWasiPolyfill());
	console.log(
		"generated packages/browser/src/wasi-polyfill.ts from shared native WASI runner",
	);
}
